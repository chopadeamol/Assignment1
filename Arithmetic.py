def Add(num1,num2):
    num=num1+num2
    print("Addition of number is :", num)

def Sub(num1,num2):
    num=num1-num2
    print("Subtraction of number is :", num)

def Mult(num1,num2):
    num=num1*num2
    print("Multiplication of number is :", num)

def Div(num1,num2):
    num=num1/num2
    print("Division of number is :", num)
